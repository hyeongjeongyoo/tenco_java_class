package basic.ch02;

public class RectangleAreaCalculatior {

	public static void main(String[] args) {

		// hint -- , 가로 길이, 세로 길이, 단 상수 사용
		// 식을 작성하고
		
		final int WIDTH = 100;
		final int HEIGHT = 100;
		
		int area = WIDTH * HEIGHT;
		
		// 결과 출력하기
		System.out.println("사각형의 면적 : " + area );
		
		// 문자열은 특벽한 녀식이라 연산을 사용할 수 있다.
		// 문자열 + 정수, 문자열 + 실수 ...
		// 위 연산에 결과 값은 --> 문자열 리턴된다.
		
	}//end of main

}// end of class
